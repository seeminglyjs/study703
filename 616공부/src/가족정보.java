
public class 가족정보 {
	
	private String name;
	private String job;
	

	
	public 가족정보(String name, String job) {
		super();
		this.name = name;
		this.job = job;
	}
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}

}
